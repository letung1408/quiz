    private void btnInsertScheduleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertScheduleActionPerformed
        // TODO add your handling code here:        
        Date date = new Date();
        Examination exam = new Examination();
        exam.setName(txtName.getText());
        exam.setDescription(txtDescription.getText());
        exam.setCreateTime(date);
        exam.setUpdateTime(date);
        if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(manageSchedulePanel,"Please input name of examination!","ERROR!",JOptionPane.ERROR_MESSAGE);
        }
        else if(txtDescription.getText().equals("")){
            JOptionPane.showMessageDialog(manageSchedulePanel,"Please input description of examination!","ERROR!",JOptionPane.ERROR_MESSAGE);
        }else if(examService.Create(exam) != null){
            JOptionPane.showMessageDialog(manageSchedulePanel,"Create Examination Successfully !","INFO!",JOptionPane.INFORMATION_MESSAGE);
            LoadTable();
        }else{
            JOptionPane.showMessageDialog(manageSchedulePanel,"A mistake while create examination!","ERROR!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertScheduleActionPerformed


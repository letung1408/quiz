    public void LoadCombobox(){
        List<Examination> listExamination = serviceExamination.GetAll();
        DefaultComboBoxModel modelExam = new DefaultComboBoxModel();
        for (Entities.Examination item : listExamination) {      
            modelExam.addElement(item.getName());
        }
        
        cbbExamination.setModel(modelExam);
        
        //subject lam tuong tu
        List<Subject> litSubject = serviceSubject.GetAll();
        DefaultComboBoxModel modelSubject = new DefaultComboBoxModel();
        for(Entities.Subject item : litSubject){   
            
            modelSubject.addElement(item.getName());
        }
         
        cbbSubject.setModel(modelSubject);                     
               
    }        


    public void LoadTable(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Name");
        model.addColumn("Examination");
        model.addColumn("Subject");
        model.addColumn("Number Question");
        model.addColumn("Duration");
        
        
        tbExam.setModel(model);
        // get all subject
        List<Entities.Exam> listExam = serviceExam.GetAll();
        for (Entities.Exam item : listExam) {
            model.addRow(new Object[]{
                item.getName(),
                item.getExamination(),
                item.getSubject(),
                item.getNumberOfQuestion(),
                item.getDuration(),
      
            });
        }
    }      
            

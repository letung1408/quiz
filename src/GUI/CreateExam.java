    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        Date date = new Date();
        Examination examination = serviceExamination.GetExamination(cbbExamination.getSelectedItem().toString());
        Subject subject = serviceSubject.GetSubject(cbbSubject.getSelectedItem().toString());
        UserAccount useraccount = new UserAccount();
        
        Exam exam = new Exam();
        
        exam.setName(txtName.getText());
        exam.setNumberOfQuestion(Integer.parseInt(txtNumberOfquestion.getText()));
        exam.setDuration(Double.parseDouble(txtDuration.getText()));
        exam.setCreateTime(date);
        exam.setUpdateTime(date);
        exam.setSubject(subject);
        exam.setExamination(examination);
        //exam.setUserAccount(useraccount);
        if (txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(ManagementExam, "Please input name of exam!", "ERROR!", JOptionPane.ERROR_MESSAGE);
        } else if (txtNumberOfquestion.getText().equals("")) {
            JOptionPane.showMessageDialog(ManagementExam, "Please input number of question of exam!", "ERROR!", JOptionPane.ERROR_MESSAGE);
        } else if (txtDuration.getText().equals("")) {
            JOptionPane.showMessageDialog(ManagementExam, "Please input useraccount of exam!", "ERROR!", JOptionPane.ERROR_MESSAGE);
        } else if (serviceExam.Create(exam) != null) {
            JOptionPane.showMessageDialog(ManagementExam, "Create Exam Successfully !", "INFO!", JOptionPane.INFORMATION_MESSAGE);
            LoadTable();
        } else {            
            JOptionPane.showMessageDialog(ManagementExam, "A mistake while create examination!", "ERROR!", JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_btnCreateActionPerformed

